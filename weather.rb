todays_weather = 'sunny'
case todays_weather
when 'rainy'
	puts "Take an umbrella or just stay home!"
when 'sunny'
	puts "Yay! Vitamin D and skin cancer!"
when 'cloudy'
	puts "Take sunscreen AND an umbrella, and bundle up!"
when 'snowy'
	puts "Break out the tire chains! Or stay home."
else 
	puts "What can I tell you? Do what feels good to YOU!"
end